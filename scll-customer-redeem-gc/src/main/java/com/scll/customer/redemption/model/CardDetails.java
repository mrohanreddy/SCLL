package com.scll.customer.redemption.model;

public class CardDetails {
	
	private String partnerID;
	private String points;
	private String redeemptionValue;
	
	
	
	public String getPartnerID() {
		return partnerID;
	}
	public void setPartnerID(String partnerID) {
		this.partnerID = partnerID;
	}
	public String getPoints() {
		return points;
	}
	public void setPoints(String points) {
		this.points = points;
	}
	public String getRedeemptionValue() {
		return redeemptionValue;
	}
	public void setRedeemptionValue(String redeemptionValue) {
		this.redeemptionValue = redeemptionValue;
	}
	
	

}
